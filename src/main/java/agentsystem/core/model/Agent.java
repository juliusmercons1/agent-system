package agentsystem.core.model;

public class Agent {

	private String agentCode;
	private String agentId;
	private String agent_name;
	private String agentStatus;
	private String agentMinimumBalance;
	private String agentTotalCredit;
	private String agentCommRate;
	private String agentTotalComm;
	private String agentholdAmt;
	private String agentLastSettlementDate;
	
	public String getAgentCode() {
		return agentCode;
	}
	public void setAgentCode(String agentCode) {
		this.agentCode = agentCode;
	}
	public String getAgentId() {
		return agentId;
	}
	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}
	public String getAgent_name() {
		return agent_name;
	}
	public void setAgent_name(String agent_name) {
		this.agent_name = agent_name;
	}
	public String getAgentStatus() {
		return agentStatus;
	}
	public void setAgentStatus(String agentStatus) {
		this.agentStatus = agentStatus;
	}
	public String getAgentMinimumBalance() {
		return agentMinimumBalance;
	}
	public void setAgentMinimumBalance(String agentMinimumBalance) {
		this.agentMinimumBalance = agentMinimumBalance;
	}
	public String getAgentTotalCredit() {
		return agentTotalCredit;
	}
	public void setAgentTotalCredit(String agentTotalCredit) {
		this.agentTotalCredit = agentTotalCredit;
	}
	public String getAgentCommRate() {
		return agentCommRate;
	}
	public void setAgentCommRate(String agentCommRate) {
		this.agentCommRate = agentCommRate;
	}
	public String getAgentTotalComm() {
		return agentTotalComm;
	}
	public void setAgentTotalComm(String agentTotalComm) {
		this.agentTotalComm = agentTotalComm;
	}
	public String getAgentholdAmt() {
		return agentholdAmt;
	}
	public void setAgentholdAmt(String agentholdAmt) {
		this.agentholdAmt = agentholdAmt;
	}
	public String getAgentLastSettlementDate() {
		return agentLastSettlementDate;
	}
	public void setAgentLastSettlementDate(String agentLastSettlementDate) {
		this.agentLastSettlementDate = agentLastSettlementDate;
	}
	
}