package agentsystem.core.model;

public class Player {

	private String playerCode;
	private String playerId;
	private String playerName;
	private String playerStatus;
	private String playerTotalCredit;
	private String playerLastLogin;
	private String playerDateCreated;
	private String playerFirstLoad;
	private String playerLoginIp;
	
	public String getPlayerCode() {
		return playerCode;
	}
	public void setPlayerCode(String playerCode) {
		this.playerCode = playerCode;
	}
	public String getPlayerId() {
		return playerId;
	}
	public void setPlayerId(String playerId) {
		this.playerId = playerId;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public String getPlayerStatus() {
		return playerStatus;
	}
	public void setPlayerStatus(String playerStatus) {
		this.playerStatus = playerStatus;
	}
	public String getPlayerTotalCredit() {
		return playerTotalCredit;
	}
	public void setPlayerTotalCredit(String playerTotalCredit) {
		this.playerTotalCredit = playerTotalCredit;
	}
	public String getPlayerLastLogin() {
		return playerLastLogin;
	}
	public void setPlayerLastLogin(String playerLastLogin) {
		this.playerLastLogin = playerLastLogin;
	}
	public String getPlayerDateCreated() {
		return playerDateCreated;
	}
	public void setPlayerDateCreated(String playerDateCreated) {
		this.playerDateCreated = playerDateCreated;
	}
	public String getPlayerFirstLoad() {
		return playerFirstLoad;
	}
	public void setPlayerFirstLoad(String playerFirstLoad) {
		this.playerFirstLoad = playerFirstLoad;
	}
	public String getPlayerLoginIp() {
		return playerLoginIp;
	}
	public void setPlayerLoginIp(String playerLoginIp) {
		this.playerLoginIp = playerLoginIp;
	}

	
}
